int	ft_islower(int c)
{
	if ((c >= 'a') && (c <= 'z'))
		return (1);
	else
		return (0);
}

int	ft_toupper(char c)
{
	if (ft_islower(c))
		return (c - ('a' - 'A'));
	else
		return (c);
}
