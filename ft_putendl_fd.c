#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		n;
	char	c;

	c = '\n';
	if (fd == 0)
		return ;
	n = ft_strlen(s);
	if (n != 0)
		write(fd, s, n);
	write(fd, &c, 1);
}
